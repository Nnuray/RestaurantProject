public class ChineseCuisine implements Dish{
    private String name;
    private int price;

    public ChineseCuisine(String name, int price) {
        this.name = name;
        this.price = price;
    }

    @Override
    public String getName() {
        return name;
    }

    @Override
    public int getPrice() {
        return price;
    }

}
